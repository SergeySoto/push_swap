#include "libft.h"

int	ft_pf_putstr(char *s)
{
	int	i;

	i = 0;
	if (!s)
	{
		write (1, "(null)", 6);
		return (6);
	}
	while (s[i] != '\0')
		i += ft_pf_putchar(s[i]);
	return (i);
}
